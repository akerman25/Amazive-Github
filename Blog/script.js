/* SHOW WRITE ARTICLE */

function showWriteArticle () {
        document.getElementById('write-article-container').style.display = "block"; //show the container
        document.getElementById('write-article-background-layer').style.display = "block"; // show dark background layer
    }
    
function hideWriteArticle () {
        document.getElementById('write-article-container').style.display = "none"; // hide the container
        document.getElementById('write-article-background-layer').style.display = "none"; // hide dark background-layer
    }
    
    

